package com.plan.city;

public class CityDTO {
	private int no;
	private String big_name;
	private String city_name;
	private double city_xlocation;
	private double city_ylocation;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getBig_name() {
		return big_name;
	}
	public void setBig_name(String big_name) {
		this.big_name = big_name;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public double getCity_xlocation() {
		return city_xlocation;
	}
	public void setCity_xlocation(double city_xlocation) {
		this.city_xlocation = city_xlocation;
	}
	public double getCity_ylocation() {
		return city_ylocation;
	}
	public void setCity_ylocation(double city_ylocation) {
		this.city_ylocation = city_ylocation;
	}
	
	
}
