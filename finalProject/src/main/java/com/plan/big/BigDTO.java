package com.plan.big;

public class BigDTO {
	private int no;
	private String bid_name;
	private	float big_xlocation;
	private float big_ylocation;
	private float big_ename;
	
	
	
	public float getBig_xlocation() {
		return big_xlocation;
	}
	public void setBig_xlocation(float big_xlocation) {
		this.big_xlocation = big_xlocation;
	}
	public float getBig_ylocation() {
		return big_ylocation;
	}
	public void setBig_ylocation(float big_ylocation) {
		this.big_ylocation = big_ylocation;
	}
	public float getBig_ename() {
		return big_ename;
	}
	public void setBig_ename(float big_ename) {
		this.big_ename = big_ename;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getBid_name() {
		return bid_name;
	}
	public void setBid_name(String bid_name) {
		this.bid_name = bid_name;
	}
	
}
